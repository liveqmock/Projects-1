$(function() {
	MediaplayManager = {				
				addCheck : function() {					
					return true;
				},
				
				addMediaplay : function() {
					window.open(WWWROOT + "/mediaplay/add_form");
				},
				
				editMediaplay : function(grid, rowData) {
					window.open(WWWROOT+ "/mediaplay/edit_form?mediaplayId="+ rowData.mediaplayId);
				},
				
				formatViewLink : function(val, cell, colpos, rwdat, _act) {
					return "<a href=\"javascript:window.open('" + WWWROOT
							+ "/mediaplay/view_form?mediaplayId="
							+ cell.rowId + "');void(0);\">" + val + "</a>";
				},
				
				delCheck : function() {
					return true;
				},
				
				deleteMediaplay : function(grid, mediaplayIds) {
					if (!confirm("确认要删除选中记录吗？")) {
						return;
					}
					$.ajax({
								type : "post",
								url : WWWROOT + "/mediaplay/delete",
								data : {
									mediaplayIds: mediaplayIds
								},
								cache : false,
								dataType : 'json',
								success : function(data) {
									if (data && data.success) {
										$.alert("已经删除成功");
										MediaplayManager.reloadGrid();
									} else {
										$.alert("删除失败，原因：" + data.msg);
									}
								},
								error : function(XMLHttpRequest, textStatus,
										errorThrown) {
									$.alert("删除失败，请稍后再次尝试删除！");
								}
							});
				},											
				reloadGrid : function() {
					$("#mediaplayListGrid").jqGrid("setGridParam", {
								page : 1,
								postData : {
									
								}
							}).trigger("reloadGrid");
				},
				callBack : function() {
					$.alert("提交成功");
				}
			}
		});