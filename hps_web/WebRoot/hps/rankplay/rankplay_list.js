$(function() {
	RankplayManager = {				
				addCheck : function() {					
					return true;
				},
				
				addRankplay : function() {
					window.open(WWWROOT + "/rankplay/add_form");
				},
				
				editRankplay : function(grid, rowData) {
					window.open(WWWROOT+ "/rankplay/edit_form?rankplayId="+ rowData.rankplayId);
				},
				
				formatViewLink : function(val, cell, colpos, rwdat, _act) {
					return "<a href=\"javascript:window.open('" + WWWROOT
							+ "/rankplay/view_form?rankplayId="
							+ cell.rowId + "');void(0);\">" + val + "</a>";
				},
				
				delCheck : function() {
					return true;
				},
				
				deleteRankplay : function(grid, rankplayIds) {
					if (!confirm("确认要删除选中记录吗？")) {
						return;
					}
					$.ajax({
								type : "post",
								url : WWWROOT + "/rankplay/delete",
								data : {
									rankplayIds: rankplayIds
								},
								cache : false,
								dataType : 'json',
								success : function(data) {
									if (data && data.success) {
										$.alert("已经删除成功");
										RankplayManager.reloadGrid();
									} else {
										$.alert("删除失败，原因：" + data.msg);
									}
								},
								error : function(XMLHttpRequest, textStatus,
										errorThrown) {
									$.alert("删除失败，请稍后再次尝试删除！");
								}
							});
				},											
				reloadGrid : function() {
					$("#rankplayListGrid").jqGrid("setGridParam", {
								page : 1,
								postData : {
									
								}
							}).trigger("reloadGrid");
				},
				callBack : function() {
					$.alert("提交成功");
				}
			}
		});